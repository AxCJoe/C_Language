#include <stdio.h>

int main()

{
    //* 条件运算符
    //* 语法: exp1 ? exp2 : exp3;
    /*
    * -- exp1是条件表达式
    * -- 如果结果为真返回,返回exp2
    * -- 结果结果为假返回,返回exp3
    */ 
    
    printf("%d\n",10>8?1:2);
    printf("%d\n",10<8?1:2);
    return 0;
}
